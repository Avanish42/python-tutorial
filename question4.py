# Step 1: Create the list
numbers = [10, 20, 30, 40]
print("Initial list:", numbers)

# Step 2: Add 50 to the list
numbers.append(50)
print("After adding 50:", numbers)

# Step 3: Remove 20 from the list
numbers.remove(20)
print("After removing 20:", numbers)

# Step 4: Access the 2nd element (index 1)
second_element = numbers[1]
print("2nd element in the list:", second_element)

